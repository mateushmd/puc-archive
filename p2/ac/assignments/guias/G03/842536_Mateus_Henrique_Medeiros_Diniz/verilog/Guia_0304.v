	SaÃ­da:
	
	a) 0000000000011001 - 0000000000001101 = 0000000000001100
	b) 101.1101 - 10.0100 = 011.1001
	c) 0000000000110110 - 0000000000101101 = 1001
	d) 000376 - 000267 = 000107
	e) 07d2 - 0a51 = -d81
